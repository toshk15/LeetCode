class Solution:
    def totalMoney(self, n: int) -> int:
        monday=1
        total=0
        x=1
        ii=0
        for i in range(n):
            if i//7==1*x:
                monday+=1
                x+=1   
                ii=0         
            total+= monday+ii
            ii+=1
        
        return total
